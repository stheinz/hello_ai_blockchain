# Create contract instance
contract = web3.eth.contract(address=contract_address, abi=abi)

# Function to hash the image
def hash_image(file_path):
    with open(file_path, 'rb') as f:
        bytes = f.read()
        hash = hashlib.sha256(bytes).hexdigest()
    return hash

@app.route('/upload', methods=['POST'])
def upload_image():
    image = request.files['image']
    image_path = os.path.join('uploads', image.filename)
    image.save(image_path)

    # Hash the image
    image_hash = hash_image(image_path)

    # Check if image already exists
    is_registered = contract.functions.verifyImage(image_hash).call()
    if is_registered:
        return jsonify({'message': 'Image already exists as NFT'}), 400

    # Mint NFT
    token_uri = 'ipfs://path_to_metadata'  # Metadata URL (e.g., IPFS)
    tx_hash = contract.functions.mintNFT(web3.eth.accounts[0], token_uri, image_hash).transact({'from': web3.eth.accounts[0]})
    web3.eth.waitForTransactionReceipt(tx_hash)

    return jsonify({'message': 'NFT created successfully', 'image_hash': image_hash}), 200

